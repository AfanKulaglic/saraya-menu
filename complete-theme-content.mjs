function findContentBlockEnd(source, searchFrom) {
  const contentIdx = source.indexOf('content: {', searchFrom);
  if (contentIdx === -1) return -1;
  
  let depth = 0;
  let i = source.indexOf('{', contentIdx);
  for (; i < source.length; i++) {
    if (source[i] === '{') depth++;
    if (source[i] === '}') {
      depth--;
      if (depth === 0) return i;
    }
  }
  return -1;
}

